package practice;

public class Escape {
	
	public static void main (String [] args) {
		
		System.out.println("This is escape method \tin JAVA");
		System.out.println("This is escape method \nin JAVA");
		System.out.println("This is escape method \tin JAVA");
		System.out.println("This is escape method \'in JAVA\'");
		System.out.println("This is escape method \"in JAVA\"");
		System.out.println("This is escape method \\in JAVA");
		System.out.println("This is escape method \r--in JAVA");
		
	}

}
